const formulario = document.getElementById("formulario");

const inputNombre = document.getElementById("nombre");

const mensajeError = document.getElementById("mensaje-error");

const pantallaLogin = document.getElementById("pantalla-login");

const pantallaOpciones = document.getElementById("pantalla-opciones");

const saludo = document.getElementById("saludo");


// Verifica si ya existe usuario guardado
const usuarioGuardado = localStorage.getItem("usuario");

if(usuarioGuardado){

    pantallaLogin.classList.add("oculto");

    pantallaOpciones.classList.remove("oculto");

    saludo.textContent = `Bienvenido/a ${usuarioGuardado}`;
}

// Carrito funciones


function obtenerCarrito() {
    let carritoString = localStorage.getItem("carrito");
    
    if (carritoString === null) {
        return [];
    }
    
    return JSON.parse(carritoString);
}

function agregarAlCarrito(producto) {
    let carrito = obtenerCarrito();
    
    let productoExistente = carrito.find(p => p.nombre === producto.nombre);
    
    if (productoExistente) {
        productoExistente.cantidad++;
    } else {
        carrito.push({ ...producto, cantidad: 1 });
    }
    
    localStorage.setItem("carrito", JSON.stringify(carrito));
    alert(producto.nombre + " agregado al carrito correctamente");
}

// Evento submit
formulario.addEventListener("submit", function(e){

    e.preventDefault();

    let nombreUsuario = inputNombre.value.trim();

    // Validar vacío
    if(nombreUsuario === ""){

        mensajeError.textContent = "El nombre no puede estar vacío";

        return;
    }

    // Validar caracteres
    for(let i = 0; i < nombreUsuario.length; i++){

        let codigo = nombreUsuario.charCodeAt(i);

        let mayuscula = codigo >= 65 && codigo <= 90;

        let minuscula = codigo >= 97 && codigo <= 122;

        let espacio = codigo === 32;

        if(!mayuscula && !minuscula && !espacio){

            mensajeError.textContent = "Solo se permiten letras";

            return;
        }

    }

    mensajeError.textContent = "";

    // Guardar usuario
    localStorage.setItem("usuario", nombreUsuario);

    // Mostrar saludo
    saludo.textContent = `Bienvenido/a ${nombreUsuario}`;

    pantallaLogin.classList.add("oculto");

    pantallaOpciones.classList.remove("oculto");

});


// Logout
document.getElementById("btn-logout").addEventListener("click", function(){

    localStorage.removeItem("usuario");

    pantallaOpciones.classList.add("oculto");

    pantallaLogin.classList.remove("oculto");

    inputNombre.value = "";

});
